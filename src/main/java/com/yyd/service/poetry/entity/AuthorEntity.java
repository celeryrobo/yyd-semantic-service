package com.yyd.service.poetry.entity;

public class AuthorEntity {
	private Integer id;
	private String name;
	private String caodai;

	public Integer getId() {
		return id;
	}

	public void setId(Integer id) {
		this.id = id;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public String getCaodai() {
		return caodai;
	}

	public void setCaodai(String caodai) {
		this.caodai = caodai;
	}
}
